# ============================================================
# Exercicio 08 - Iguais ou Diferentes
# ============================================================
# Enunciado:
#   Leia dois numeros e verifique se sao iguais ou diferentes.
#
# Conceitos praticados:
#   - Operador de igualdade (==)
#   - Operador de diferenca (!=)
# ============================================================

# Passo 1: Ler os dois numeros
a = int(input("Digite o primeiro numero: "))
b = int(input("Digite o segundo numero: "))

# Passo 2: Comparar
if a == b:
    print("Os numeros sao IGUAIS")
else:
    print("Os numeros sao DIFERENTES")
