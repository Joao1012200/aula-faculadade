numero1 = 20
numero2 = 2

#soma
resultado = numero1 + numero2
print(resultado)

#subtração
resultado = numero1 - numero2
print(resultado)

#multiplicação
resultado = numero1 * numero2
print(resultado)

#divisão
resultado = numero1 / numero2
print(resultado)

#exponenciação
resultado = numero1 ** numero2
print(resultado)

#módulo
resultado = numero1 % numero2
print(resultado)